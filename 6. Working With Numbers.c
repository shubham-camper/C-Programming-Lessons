#include <stdio.h>

int main()
{
    printf("%f\n", 8.9);
    printf("%f\n", 5.0 + 4.0);
    printf("%d\n", 5/4);
    printf("%f\n", 5/4.0);
    
    int num = 6;
    printf("%d\n", num);
    printf("%f\n", pow(2, 3) );
    printf("%f\n", pow(4, 3) );
    printf("%f\n", sqrt(36) );
    printf("%f\n", ceil(36.356) );
    printf("%f\n", floor(36.899));
    return 0;
}

